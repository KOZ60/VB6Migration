CREATE OR REPLACE PROCEDURE     PROC_NVARCHAR
(
	INPUT_VALUE 	IN	NVARCHAR2
,	OUTPUT_VALUE 	OUT NVARCHAR2
)
IS
V1	VARCHAR2(10)	:=	'CODE1';
V2	VARCHAR2(10)	:=	'CODE2';
NV1	NVARCHAR2(10)	:=	'CODE1';
NV2	NVARCHAR2(10)	:=	'CODE2';
BEGIN
	OUTPUT_VALUE	:=	INPUT_VALUE;
	FOR R IN	(SELECT * FROM TBL_MASTER WHERE CODE = NV1)
	LOOP
		DBMS_OUTPUT.PUT_LINE(R.CODE);
	END LOOP;
	FOR R IN	(SELECT * FROM TBL_MASTER WHERE CODE > NV1)
	LOOP
		DBMS_OUTPUT.PUT_LINE(R.CODE);
	END LOOP;
	IF V1 = NV1 THEN
		DBMS_OUTPUT.PUT_LINE('OK');
	ELSE
		DBMS_OUTPUT.PUT_LINE('NG');
	END IF;
	IF NV2 > V1 THEN
		DBMS_OUTPUT.PUT_LINE('OK');
	ELSE
		DBMS_OUTPUT.PUT_LINE('NG');
	END IF;
END;
/
