CREATE OR REPLACE PACKAGE     PKG_MASTER
IS
TYPE T_SEQ 		IS TABLE OF NUMBER INDEX BY BINARY_INTEGER;
TYPE T_CODE 	IS TABLE OF TBL_MASTER.CODE%TYPE INDEX BY BINARY_INTEGER;
TYPE T_NAME 	IS TABLE OF TBL_MASTER.NAME%TYPE INDEX BY BINARY_INTEGER;
TYPE T_KINGAKU 	IS TABLE OF NUMBER INDEX BY BINARY_INTEGER;
PROCEDURE BULK_INSERT(
	I_CODE		IN	T_CODE
,	I_NAME		IN	T_NAME
,	I_KINGAKU	IN	T_KINGAKU
);
PROCEDURE ARRAY_INSERT(
	I_CODE		IN	T_CODE
,	I_NAME		IN	T_NAME
,	I_KINGAKU	IN	T_KINGAKU
,	O_SEQ		OUT	T_SEQ
);
END;
