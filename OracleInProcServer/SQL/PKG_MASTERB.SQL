CREATE OR REPLACE PACKAGE BODY     PKG_MASTER
IS
PROCEDURE BULK_INSERT(
	I_CODE		IN	T_CODE
,	I_NAME		IN	T_NAME
,	I_KINGAKU	IN	T_KINGAKU
) IS
BEGIN
	FORALL i IN I_CODE.FIRST .. I_CODE.LAST
		INSERT INTO TBL_MASTER(SEQ, CODE, NAME, KINGAKU) 
			VALUES (SEQ_SEQ.NEXTVAL, I_CODE(i), I_NAME(i), I_KINGAKU(i));
END;
PROCEDURE ARRAY_INSERT(
	I_CODE		IN	T_CODE
,	I_NAME		IN	T_NAME
,	I_KINGAKU	IN	T_KINGAKU
,	O_SEQ		OUT	T_SEQ
) IS
BEGIN
	FOR i IN I_CODE.FIRST .. I_CODE.LAST LOOP
		INSERT INTO TBL_MASTER(SEQ, CODE, NAME, KINGAKU) 
			VALUES (SEQ_SEQ.NEXTVAL, I_CODE(i), I_NAME(i), I_KINGAKU(i));
		SELECT SEQ_SEQ.CURRVAL INTO O_SEQ(i) FROM DUAL;
	END LOOP;
END;
END;
